import json
import csv
from pathlib import Path
from typing import Dict, List, Any

class ResultExporter:
    @staticmethod
    def export_csv(output_path: Path, frame_detections: List[Dict[str, Any]], fps: float):
        """
        Exports frame-by-frame detections to CSV format.
        Columns: Frame, Timestamp, Class, Confidence, Xmin, Ymin, Xmax, Ymax
        """
        with open(output_path, "w", newline="", encoding="utf-8") as f:
            writer = csv.writer(f)
            writer.writerow(["Frame", "Timestamp (s)", "Class", "Confidence", "Xmin", "Ymin", "Xmax", "Ymax"])
            for item in frame_detections:
                frame_no = item.get("frame")
                timestamp = round(frame_no / fps, 2) if fps > 0 else 0.0
                for det in item.get("detections", []):
                    cls_name = det.get("class")
                    conf = det.get("confidence")
                    bbox = det.get("bbox", [0, 0, 0, 0])
                    writer.writerow([frame_no, timestamp, cls_name, conf, bbox[0], bbox[1], bbox[2], bbox[3]])

    @staticmethod
    def export_json(output_path: Path, results_data: Dict[str, Any]):
        """
        Exports full results metadata and detection array to JSON.
        """
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(results_data, f, indent=2)

    @staticmethod
    def export_summary_report(output_path: Path, results_data: Dict[str, Any]):
        """
        Generates a human-readable underwater object detection summary report.
        """
        summary = results_data.get("summary", {})
        metadata = results_data.get("video_metadata", {})
        model_info = results_data.get("model_info", {})
        timeline = results_data.get("timeline", [])

        lines = [
            "=========================================================",
            "       UNDERWATER OBJECT DETECTION SUMMARY REPORT        ",
            "=========================================================",
            f"Job ID:             {results_data.get('job_id')}",
            f"Video Filename:     {metadata.get('filename')}",
            f"Video Resolution:   {metadata.get('resolution')}",
            f"Video Duration:     {metadata.get('duration_str')}",
            f"Total Frames:       {metadata.get('total_frames')}",
            "",
            "---------------- MODEL CONFIGURATION --------------------",
            f"Model Selected:     {model_info.get('model_name')}",
            f"Model Version:      {model_info.get('version')}",
            f"Confidence Thresh:  {results_data.get('settings', {}).get('confidence_threshold')}",
            f"IoU Threshold:      {results_data.get('settings', {}).get('iou_threshold')}",
            f"Target Device:      {results_data.get('settings', {}).get('device')}",
            "",
            "---------------- DETECTION METRICS ----------------------",
            f"Total Detections:   {summary.get('total_detections')}",
            f"Frames with Objects:{summary.get('frames_with_objects')} / {metadata.get('total_frames')} ({summary.get('detection_ratio_pct')}%)",
            f"Average Confidence: {summary.get('average_confidence')}%",
            f"Processing Time:    {summary.get('processing_time_sec')} sec",
            f"Processing Speed:   {summary.get('processing_fps')} FPS",
            "",
            "---------------- CLASS DISTRIBUTION ---------------------"
        ]

        class_counts = summary.get("class_counts", {})
        for cls_name, count in class_counts.items():
            lines.append(f"  * {cls_name:<18}: {count} detections")

        lines.extend([
            "",
            "---------------- TIMELINE LOG (HIGHLIGHTS) --------------"
        ])

        for event in timeline[:25]:
            lines.append(f"  [{event.get('timestamp')}] {event.get('label')} (Conf: {int(event.get('confidence') * 100)}%)")

        if len(timeline) > 25:
            lines.append(f"  ... and {len(timeline) - 25} more detection events.")

        lines.extend([
            "",
            "=========================================================",
            " Report Generated by NIOT HITL Object Detection Portal   ",
            "========================================================="
        ])

        with open(output_path, "w", encoding="utf-8") as f:
            f.write("\n".join(lines))

result_exporter = ResultExporter()
